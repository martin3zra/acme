func (i *SalesCustomerWiseWithInvoicesPDF) Lines(groups []CustomerGroup) {
	if i.pdf.PageNo() == 0 {
		i.pdf.AddPage()
	}

	i.pdf.SetFont("DejaVu", "B", 14)
	i.pdf.CellFormat(0, 10, "Sales Report: By Customer", "", 1, "", false, 0, "")

	var grandTotal float64
	for _, group := range groups {
		// Estimate row height: header + at least one invoice line
		headerHeight := 10.0
		invoiceRowHeight := 8.0

		// Check if there's room for header + at least one invoice row
		if i.NeedsNewPage(headerHeight + invoiceRowHeight) {
			i.pdf.AddPage()
		}

		// Print customer header
		i.pdf.SetFont("DejaVu", "B", 12)
		i.pdf.CellFormat(0, headerHeight, group.CustomerName, "", 1, "", false, 0, "")

		// Print invoices
		i.pdf.SetFont("DejaVu", "", 10)
		var subtotal float64
		for _, inv := range group.Invoices {
			date := inv.Date.Format("02 Jan 2006")
			left := fmt.Sprintf("%s (INV-%06d)", date, inv.InvoiceID)
			right := foundation.FormatAmount(inv.Total)

			if i.NeedsNewPage(invoiceRowHeight) {
				i.pdf.AddPage()
			}

			i.pdf.CellFormat(155, invoiceRowHeight, left, "", 0, "", false, 0, "")
			i.pdf.CellFormat(25, invoiceRowHeight, right, "", 1, "R", false, 0, "")
			subtotal += inv.Total
		}

		// Subtotal + total lines
		lineY := i.pdf.GetY()       // current vertical position
		leftMargin := 15.0          // match your SetMargins
		rightMargin := 210.0 - 15.0 // A4 width (210mm) minus right margin

		// Draw a light gray line across the page
		i.pdf.SetDrawColor(200, 200, 200) // light gray
		i.pdf.Line(leftMargin, lineY, rightMargin, lineY)

		i.pdf.SetFont("DejaVu", "B", 10)

		// Subtotal + total lines
		i.pdf.CellFormat(155, invoiceRowHeight, "", "", 0, "", false, 0, "")
		i.pdf.CellFormat(25, invoiceRowHeight, foundation.FormatAmount(subtotal), "", 1, "R", false, 0, "")
	}
	i.pdf.SetFont("DejaVu", "B", 10)
	i.pdf.CellFormat(155, 10, "TOTAL SALES", "", 0, "", false, 0, "")
	i.pdf.CellFormat(25, 10, foundation.FormatAmount(grandTotal), "", 1, "R", false, 0, "")

	i.pdf.Ln(5)
}

// UsableHeight returns the vertical space available on the current page
// after accounting for the bottom margin.
func (i *SalesCustomerWiseWithInvoicesPDF) UsableHeight() float64 {
	_, pageHeight := i.pdf.GetPageSize()
	return pageHeight - i.bottomMargin
}

// NeedsNewPage checks if the required height fits in the remaining space.
// If not, you should call AddPage() before printing.
func (i *SalesCustomerWiseWithInvoicesPDF) NeedsNewPage(requiredHeight float64) bool {
	y := i.pdf.GetY()
	return y+requiredHeight > i.UsableHeight()
}

func (i *SalesCustomerWiseWithInvoicesPDF) Output(r http.ResponseWriter) error {
	return i.pdf.Output(r)
}

type SalesReportPDF struct {
	pdf          *fpdf.Fpdf
	trans        *i18n.Translator
	sales        []*SalesByCustomerWithInvoices
	From, To     time.Time
	bottomMargin float64
	showInvoices bool
}

func NewSalesReportPDF(trans *i18n.Translator, from, to time.Time, showInvoices bool) (*SalesReportPDF, error) {
	pdf := fpdf.New("P", "mm", "A4", "")
	pdf.SetMargins(15, 20, 15)
	pdf.SetAutoPageBreak(true, 25)
	pdf.AliasNbPages("")

	if err := registerFonts(pdf); err != nil {
		return nil, err
	}

	pdf.SetFont("DejaVu", "", 11)

	return &SalesReportPDF{
		pdf:          pdf,
		trans:        trans,
		From:         from,
		To:           to,
		bottomMargin: 25,
		showInvoices: showInvoices,
	}, pdf.Error()
}

func (r *SalesReportPDF) Render(groups []CustomerGroup) {
	if r.pdf.PageNo() == 0 {
		r.pdf.AddPage()
	}

	r.pdf.SetFont("DejaVu", "B", 14)
	r.pdf.CellFormat(0, 10, "Sales Report: By Customer", "", 1, "", false, 0, "")

	var grandTotal float64
	for idx, group := range groups {
		headerHeight := 10.0
		invoiceRowHeight := 8.0

		if r.NeedsNewPage(headerHeight + invoiceRowHeight) {
			r.pdf.AddPage()
		}

		if r.showInvoices {
			// Customer header
			r.pdf.SetFont("DejaVu", "B", 12)
			r.pdf.CellFormat(0, headerHeight, group.CustomerName, "", 1, "", false, 0, "")

			// Invoices
			r.pdf.SetFont("DejaVu", "", 10)
			var subtotal float64
			for _, inv := range group.Invoices {
				if r.NeedsNewPage(invoiceRowHeight) {
					r.pdf.AddPage()
				}
				date := inv.Date.Format("02 Jan 2006")
				left := fmt.Sprintf("%s (INV-%06d)", date, inv.InvoiceID)
				right := foundation.FormatAmount(inv.Total)

				r.pdf.CellFormat(155, invoiceRowHeight, left, "", 0, "", false, 0, "")
				r.pdf.CellFormat(25, invoiceRowHeight, right, "", 1, "R", false, 0, "")
				subtotal += inv.Total
			}

			// 🔑 set group.Total so grand total works
			group.Total = subtotal

			// Separator line above subtotal
			lineY := r.pdf.GetY()
			r.pdf.SetDrawColor(200, 200, 200)
			r.pdf.Line(15, lineY, 195, lineY)

			// Subtotal line
			r.pdf.SetFont("DejaVu", "B", 10)
			r.pdf.CellFormat(155, invoiceRowHeight, "TOTAL SALES", "", 0, "", false, 0, "")
			r.pdf.CellFormat(25, invoiceRowHeight, foundation.FormatAmount(group.Total), "", 1, "R", false, 0, "")
			r.pdf.Ln(5)

		} else {
			rowHeight := 8.0
			// Summary mode: name + total on same line
			r.pdf.SetFont("DejaVu", "", 11)
			r.pdf.CellFormat(155, rowHeight, group.CustomerName, "", 0, "", false, 0, "")
			r.pdf.CellFormat(25, rowHeight, foundation.FormatAmount(group.Total), "", 1, "R", false, 0, "")

			// If this is the last customer, draw separator line
			if idx == len(groups)-1 {
				lineY := r.pdf.GetY() + 2 // a little below last line
				r.pdf.SetDrawColor(200, 200, 200)
				r.pdf.Line(15, lineY, 195, lineY)
			}
		}

		grandTotal += group.Total
	}

	r.pdf.Ln(2)
	r.pdf.SetFont("DejaVu", "B", 12)
	r.pdf.CellFormat(155, 10, "GRAND TOTAL", "", 0, "", false, 0, "")
	r.pdf.CellFormat(25, 10, foundation.FormatAmount(grandTotal), "", 1, "R", false, 0, "")
}

// NeedsNewPage checks if the required height fits in the remaining space.
// If not, you should call AddPage() before printing.
func (i *SalesReportPDF) NeedsNewPage(requiredHeight float64) bool {
	y := i.pdf.GetY()
	return y+requiredHeight > i.UsableHeight()
}

// UsableHeight returns the vertical space available on the current page
// after accounting for the bottom margin.
func (i *SalesReportPDF) UsableHeight() float64 {
	_, pageHeight := i.pdf.GetPageSize()
	return pageHeight - i.bottomMargin
}
