type SalesCustomerWisePDF struct {
	pdf   *fpdf.Fpdf
	trans *i18n.Translator
	sales []*SalesByCustomer
	From  time.Time
	To    time.Time
}

func NewSalesCustomerWisePDF(trans *i18n.Translator, sales []*SalesByCustomer, from, to time.Time) (*SalesCustomerWisePDF, error) {

	pdf := fpdf.New("P", "mm", "A4", "")
	pdf.SetMargins(15, 20, 15)
	pdf.SetAutoPageBreak(true, 25)
	// Enables total page count
	pdf.AliasNbPages("")

	if err := registerFonts(pdf); err != nil {
		return nil, err
	}

	pdf.SetFont("DejaVu", "", 11)
	return &SalesCustomerWisePDF{pdf: pdf, trans: trans, sales: sales, From: from, To: to}, pdf.Error()
}

func (i *SalesCustomerWisePDF) t(key string, replacements ...i18n.Replacements) string {
	return i.trans.Trans(key, replacements...)
}

func (i *SalesCustomerWisePDF) AddLogo() {
	logoBytes, err := base64.StdEncoding.DecodeString(logoBase64)
	if err == nil {
		i.pdf.RegisterImageOptionsReader("logo", fpdf.ImageOptions{
			ImageType: "PNG",
		}, bytes.NewReader(logoBytes))
		i.pdf.ImageOptions("logo", 15, 10, 30, 0, false, fpdf.ImageOptions{
			ImageType: "PNG",
		}, 0, "")
	}
}

func (i *SalesCustomerWisePDF) Header(ctx context.Context) {
	i.pdf.SetHeaderFunc(func() {
		i.AddLogo()

		company := CurrentCompany(ctx)
		// Company Info (top left)
		i.pdf.SetFont("DejaVu", "", 10)
		i.pdf.SetXY(50, 10)
		i.pdf.MultiCell(80, 5, fmt.Sprintf("%s\n%s\n%s", company.Name, company.Address, ""), "", "", false)
		i.pdf.SetXY(50, 25)
		i.pdf.CellFormat(80, 6, fmt.Sprintf("%s: %s", i.t("companies.single.rnc_short"), company.Identifier), "", 1, "", false, 0, "")

		// Invoice Info (top right)
		i.pdf.SetXY(150, 10)
		i.pdf.CellFormat(0, 6, fmt.Sprintf("%s - %s", i.From.Format("2006-01-02"), i.To.Format("2006-01-02")), "", 1, "R", false, 0, "")

		i.pdf.Ln(12)
	})

}

func (i *SalesCustomerWisePDF) Footer(text string) {
	i.pdf.SetFooterFunc(func() {
		i.pdf.SetY(-15)
		i.pdf.SetFont("DejaVu", "I", 8)
		i.pdf.SetTextColor(0, 0, 0)
		footer := fmt.Sprintf("Page %d/{nb}", i.pdf.PageNo())
		i.pdf.CellFormat(
			10, 10, fmt.Sprintf("Generated by %s v0", text), "", 0, "L", false, 0, "",
		)
		i.pdf.CellFormat(
			0, 10, footer, "", 0, "C", false, 0, "",
		)
	})

}

func (i *SalesCustomerWisePDF) Lines() {

	i.pdf.Ln(10)
	// Table Header
	i.pdf.SetFont("DejaVu", "B", 10)
	i.pdf.SetFillColor(240, 240, 255)
	widths := []float64{155, 25}
	// Table Body
	i.pdf.SetFont("DejaVu", "", 10)
	for _, line := range i.sales {
		i.pdf.CellFormat(widths[0], 8, line.CustomerName, "0", 0, "", false, 0, "")
		i.pdf.CellFormat(widths[1], 8, foundation.FormatAmount(line.TotalSales), "0", 0, "R", false, 0, "")
		i.pdf.Ln(-1)
	}

	i.pdf.Ln(10)
}

func (i *SalesCustomerWisePDF) Output(r http.ResponseWriter) error {
	return i.pdf.Output(r)
}

